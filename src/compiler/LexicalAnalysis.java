    private boolean match(char ch,String fileName , int Line)
    {
        boolean match;
        
        match = RE.match(ch);
        if(RE.isFine())
        {

            System.out.println( "Line #: "+Line+" Token Text:  "+RE.getLexeme()+"   Token Type: "+RE.getToken()+" in file "+fileName );
            String ReturnToken=RE.getReturnToken();
            if(ReturnToken!=null)
            {
                TokenHolder token=new TokenHolder();
                token.token=ReturnToken;
                token.fileName=fileName;
                token.line=Line;
                Tokens.add(token); 
            }
            
            RE.clear();
            return match;
        }
        if(!RE.canContinue())
        {
            String ErrorLexeme=RE.getLexeme();
            System.out.println( "Line #: "+Line+" Error in Token Text:  "+ErrorLexeme+" in file "+fileName);
            RE.clear();
        }
            
            
            return true;
    }
    
    
    private int scanHave(String line) 
    {
        int index=0;
        boolean match=false;
        String fileName="";
        for(int i=0;i<line.length();i++)
        {
            char ch=line.charAt(i);
            Special.match(ch);
            if(Special.isFine())
            {
                match=true;
                index=i;
                break;
            }
            if(!Special.canContinue())
            {                        
                Special.clear();
            }
        }
        if(match)
        {
            String having=Special.getLexeme();
            for(int i=0;i<having.length();i++)
            {
                if(having.charAt(i)=='(')
                {
                    for(int j=i+1;j<having.length();j++)
                    {
                        if(having.charAt(j)==')')
                        {
                            break;
                        }
                        fileName+=having.charAt(j);
                    }
                    break;
                }
            }
            Special.clear();
            this.scanFile(fileName);
        }
        
        
        
        return index;
                
       
    }
    
    public boolean scanFile(String fileName) 
    {
        int Line =1;
        int index =0;
        try{
            Scanner sc = new Scanner(new File(fileName));
            
            String line;
            
            while(sc.hasNextLine())
            {
                
                
                line=sc.nextLine();
                index = scanHave(line+" ");
                for(int i=index;i<line.length();i++)
                {
                    char ch=line.charAt(i);
                    if(!this.match(ch,fileName,Line))
                    {
                        this.match(ch,fileName,Line);
                    }
                }
                
                if(!this.match('\n',fileName,Line))
                {
                    this.match('\n',fileName,Line);
                }
                Line++;
            }
            sc.close();
            return true;
        }
        catch(Exception e)
        {
            System.err.println(fileName + " not found");
            return false;
        }
    }
    
    public TokenHolder nextToken()
    {
        if(Tokens.size()>0)
            return Tokens.remove(0);
        return null;
    }
